async def seed(session: AsyncSession) -> dict[str, int]:
    """Insert seed rows that don't already exist (matched by text). Returns counts."""
    existing_q = await session.scalars(select(QOTDQuestion.text))
    have_q = set(existing_q)
    q_added = 0
    for category, text in QOTD_BANK:
        if text in have_q:
            continue
        session.add(QOTDQuestion(text=text, category=category, is_active=True))
        q_added += 1

    # Gift catalog is a constant (GIFT_CATALOG), not a table: GiftItem rows are per-pair
    # gift instances, not catalog entries. The bot imports GIFT_CATALOG directly.
    await session.flush()
    return {"qotd_added": q_added, "gift_catalog": len(GIFT_CATALOG)}
